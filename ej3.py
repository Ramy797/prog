#Escribe un programa en Python que solicite al usuario que ingrese la base y la altura de un triángulo, y luego calcule y muestre el área del triángulo
print("bienvenido a la calculadora de area de triangulos")
altura = float(input("ingrese la altura de su triangulo:"))
base = float(input("ingrese la base de su triangulo"))
area = base * altura / 2
print(f"el area de su triangulo es:", + area)
